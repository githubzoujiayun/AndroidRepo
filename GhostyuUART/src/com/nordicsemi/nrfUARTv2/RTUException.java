package com.nordicsemi.nrfUARTv2;

public class RTUException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public RTUException() {
		super();
	}

	public RTUException(String message, Throwable cause) {
		super(message, cause);
	}

	public RTUException(String message) {
		super(message);
	}

	public RTUException(Throwable cause) {
		super(cause);
	}
	
	

}
